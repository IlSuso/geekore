export type ApiMessageKey = keyof typeof API_MESSAGES.it

type LocaleRequestLike = { headers?: { get(name: string): string | null } | Headers; cookies?: { get(name: string): { value?: string } | string | undefined }; nextUrl?: { searchParams?: { get(name: string): string | null } } }

function headerGet(headers: LocaleRequestLike['headers'], name: string): string | null {
  return headers?.get(name) ?? null
}

function cookieGet(cookies: LocaleRequestLike['cookies'], name: string): string | null {
  const value = cookies?.get(name)
  if (!value) return null
  return typeof value === 'string' ? value : value.value || null
}

export function requestLocaleSync(request: LocaleRequestLike | null | undefined): Locale {
  if (!request) return 'it'
  return (
    normalizeLocale(request.nextUrl?.searchParams?.get('lang')) ||
    normalizeLocale(headerGet(request.headers, 'x-lang') || headerGet(request.headers, 'x-geekore-locale')) ||
    normalizeLocale(cookieGet(request.cookies, 'geekore_locale')) ||
    normalizeLocale(headerGet(request.headers, 'accept-language')?.split(',')[0]) ||
    'it'
  )
}

export function apiMessage(request: LocaleRequestLike | null | undefined, key: ApiMessageKey): string {
  const locale = requestLocaleSync(request)
  return API_MESSAGES[locale]?.[key] || API_MESSAGES.it[key]
}

export function apiMessageForLocale(locale: Locale | null | undefined, key: ApiMessageKey): string {
  return API_MESSAGES[locale || 'it']?.[key] || API_MESSAGES.it[key]
}
